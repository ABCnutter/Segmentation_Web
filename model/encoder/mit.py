def _mit_extractor(name: str = "mitb0",
                   predicted: bool = False,
                   multiple_features_return: bool = True,
                   ):
    model_lists = ["mitb0", "mitb1", "mitb2", "mitb3", "mitb4", "mitb5"]

    if name not in model_lists:
        raise ValueError(f"Please select name in {model_lists}")

    params = mix_transformer_encoders[name]["params"]
    params.update(multiple_features_return=multiple_features_return)
    encoder = MixVisionTransformer(**params)

    if predicted:
        settings = mix_transformer_encoders[name]["pretrained_settings"]["imagenet"]
        state_dict = model_zoo.load_url(settings["url"])
        for key, value in list(state_dict.items()):
            if key in ["head.weight", "head.bias"]:
                state_dict.pop(key)
        encoder.load_state_dict(state_dict)

    return encoder


